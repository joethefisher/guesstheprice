// Stable shuffle by id-hash so different sessions get different orderings
// without ever showing the same product twice in a single run.
export function shuffleDeck(seed = Date.now()): AmazonProduct[] {
  const arr = [...PRODUCTS];
  let s = seed % 2147483647;
  for (let i = arr.length - 1; i > 0; i--) {
    s = (s * 16807) % 2147483647;
    const j = s % (i + 1);
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
